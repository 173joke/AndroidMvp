package com.dsy.mvp.bean;

/**
 * 继承 {@link BaseBean}
 */
public class SimpleBean extends BaseBean {

    /**
     * data : {"id":123456,"name":"张三","age":18}
     */

    private DataBean data;

    public DataBean getData() {
        return data;
    }

    public void setData(DataBean data) {
        this.data = data;
    }

    public static class DataBean {
        /**
         * id : 123456
         * name : 张三
         * age : 18
         */

        private int id;
        private String name;
        private int age;

        public int getId() {
            return id;
        }

        public void setId(int id) {
            this.id = id;
        }

        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }

        public int getAge() {
            return age;
        }

        public void setAge(int age) {
            this.age = age;
        }
    }
}
